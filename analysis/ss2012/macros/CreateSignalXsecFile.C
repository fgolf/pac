void SetBinContent(TH1* h, const float bin_value, const float xsec, const float xsec_unc_perc)
{
    int bin = h->FindBin(bin_value + 0.00001);
    h->SetBinContent(bin, xsec);
    h->SetBinError(bin, xsec*0.01*xsec_unc_perc);
    return;
}

